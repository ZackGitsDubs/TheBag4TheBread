
public class Calculator {
	public int add(int intAdd1, int intAdd2) {
		return intAdd1 + intAdd2;
	}
	
	public static int subtract(int intSub1, int intSub2) {
		return intSub1 - intSub2;
	}
	
	public static int multiply(int intMult1, int intMult2) {
		return intMult1 * intMult2;
	}
	
	public static int divide(int intDiv1, int intDiv2) {
		return intDiv1 - intDiv2;
	}
}
